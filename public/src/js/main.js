$(document).ready(function(){
	$('.btn-shortner').click(function() {
		isUrlValid($('input').val()) ? createNew() : showError();
	});
	$("body").on('click', '#link', function() {
	  	$(this).select();
	});
});

function createNew() {
	$('.wrapper').show();
	$.ajax({
	      type: "POST",
	      url: "/new",
	      data: {
	      		url : $('input').val()
	      },
	      success: function(resultData){
	          $('.wrapper').hide();
	          $('#link').html("<span style='color: #555;'>SHORT LINK</span><br><br>" + window.location.host + "/" + resultData['data']);
	          $('input').val('');
	      }
	});
}

